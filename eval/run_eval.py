#!/usr/bin/env python3

from __future__ import annotations

import argparse
import json
import os
import platform
import subprocess
from datetime import datetime, timezone
from pathlib import Path
from typing import Any


def load_thresholds(path: Path) -> dict[str, float]:
    thresholds: dict[str, float] = {}
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#"):
            continue
        key, value = line.split(":", 1)
        thresholds[key.strip()] = float(value.strip())
    return thresholds


def load_shapes(path: Path) -> list[dict[str, int]]:
    shapes: list[dict[str, int]] = []
    current: dict[str, int] | None = None
    for raw_line in path.read_text(encoding="utf-8").splitlines():
        line = raw_line.strip()
        if not line or line.startswith("#") or line.endswith(":"):
            continue
        if line.startswith("- "):
            if current:
                shapes.append(current)
            current = {}
            line = line[2:]
            if line:
                key, value = line.split(":", 1)
                current[key.strip()] = int(value.strip())
            continue
        key, value = line.split(":", 1)
        if current is None:
            current = {}
        current[key.strip()] = int(value.strip())
    if current:
        shapes.append(current)
    return shapes


def git_commit(cwd: Path) -> str:
    try:
        return subprocess.check_output(
            ["git", "rev-parse", "HEAD"],
            cwd=cwd,
            text=True,
        ).strip()
    except Exception:
        return "unknown"


def run_binary(path: Path) -> dict[str, Any]:
    if not path.exists():
        return {"status": "missing", "binary": str(path)}
    try:
        completed = subprocess.run(
            [str(path)],
            check=True,
            text=True,
            capture_output=True,
        )
        return {
            "status": "ok",
            "binary": str(path),
            "stdout": completed.stdout.strip(),
        }
    except subprocess.CalledProcessError as exc:
        return {
            "status": "failed",
            "binary": str(path),
            "stdout": exc.stdout.strip(),
            "stderr": exc.stderr.strip(),
            "returncode": exc.returncode,
        }


def make_report(repo_root: Path, args: argparse.Namespace) -> dict[str, Any]:
    thresholds = load_thresholds(repo_root / "eval" / "thresholds.yaml")
    shapes = load_shapes(repo_root / "bench" / "shapes.yaml")
    build_dir = repo_root / "build"

    benches = {
        "transform": run_binary(build_dir / "cco_bench_transform"),
        "compute": run_binary(build_dir / "cco_bench_compute"),
        "pipeline": run_binary(build_dir / "cco_bench_pipeline"),
    }

    baseline_path = repo_root / "eval" / "baselines" / "0.0.0.json"

    return {
        "repo": "Cuda-Compute-OSS",
        "transform_version": args.transform_version,
        "timestamp_utc": datetime.now(timezone.utc).isoformat(),
        "git_commit": git_commit(repo_root),
        "host": platform.node(),
        "platform": platform.platform(),
        "python": platform.python_version(),
        "gpu": os.environ.get("CCO_GPU", "unknown"),
        "cuda": os.environ.get("CCO_CUDA", "unknown"),
        "dtype": os.environ.get("CCO_DTYPE", "fp32"),
        "shapes": shapes,
        "thresholds": thresholds,
        "baseline_path": str(baseline_path),
        "benchmarks": benches,
        "verdict": "scaffold",
        "notes": [
            "This report is generated by the current CCO eval scaffold.",
            "Replace scaffold fields with measured GEMM and transform metrics as kernels mature.",
        ],
    }


def write_markdown(path: Path, report: dict[str, Any]) -> None:
    benches = report["benchmarks"]
    lines = [
        "# CCO Eval Report",
        "",
        f"- transform version: `{report['transform_version']}`",
        f"- git commit: `{report['git_commit']}`",
        f"- gpu: `{report['gpu']}`",
        f"- cuda: `{report['cuda']}`",
        f"- dtype: `{report['dtype']}`",
        f"- verdict: `{report['verdict']}`",
        "",
        "## Shapes",
        "",
    ]
    for shape in report["shapes"]:
        lines.append(f"- n={shape['n']}, m={shape['m']}")
    lines.extend(["", "## Benchmarks", ""])
    for name, result in benches.items():
        lines.append(f"- `{name}`: {result['status']}")
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def main() -> None:
    parser = argparse.ArgumentParser(description="Run CCO eval and write a machine-readable report.")
    parser.add_argument("--transform-version", default="0.0.0")
    parser.add_argument("--report-path", default="eval/reports/latest.json")
    parser.add_argument("--markdown-path", default="eval/reports/latest.md")
    args = parser.parse_args()

    repo_root = Path(__file__).resolve().parents[1]
    report = make_report(repo_root, args)

    report_path = repo_root / args.report_path
    markdown_path = repo_root / args.markdown_path
    report_path.parent.mkdir(parents=True, exist_ok=True)
    markdown_path.parent.mkdir(parents=True, exist_ok=True)

    report_path.write_text(json.dumps(report, indent=2) + "\n", encoding="utf-8")
    write_markdown(markdown_path, report)

    print("CCO eval")
    print(f"json report: {report_path}")
    print(f"markdown report: {markdown_path}")
    print(f"verdict: {report['verdict']}")


if __name__ == "__main__":
    main()
